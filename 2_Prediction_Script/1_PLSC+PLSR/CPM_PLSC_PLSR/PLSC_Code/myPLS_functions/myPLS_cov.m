function R = myPLS_cov(X, Y, grouping, grouped_PLS)
% =============================
% 功能：
% 生成PLS分析所需的交叉协方差矩阵R。支持分组与非分组模式，分组时将各组协方差矩阵按行拼接。
%
% =============================
% 输入：
% X           ：N×M矩阵，影像数据
%               N：受试者数量
%               M：影像变量数量
% Y           ：N×B矩阵，行为/设计数据
%               B：行为/设计变量数量
% grouping    ：N×1向量，受试者分组信息
%               例如[1,1,2]表示前两名为组1，第三名为组2
% grouped_PLS ：逻辑值/0或1，是否分组PLS
%               0 = 忽略分组，整体计算协方差
%               1 = 各组分别计算协方差并拼接
%
% =============================
% 输出：
% R           ：交叉协方差矩阵
%               非分组时为B×M矩阵，分组时为(G×B)×M矩阵（G为组数）
%
% =============================
% 示例：
% R = myPLS_cov(X, Y, grouping, grouped_PLS);
%
% =============================
% 历史：
% 2024-06-09  初版，标准化注释与格式，详细参数说明。
% =============================

%% ========== 检查是否为分组PLS ========== %%
if ~grouped_PLS
    grouping = ones(size(grouping)); % 若非分组，所有受试者视为一组
end

%% ========== 获取分组信息 ========== %%
groupIDs = unique(grouping); % 所有组的编号
groups = length(groupIDs);   % 组数

%% ========== 计算各组交叉协方差并拼接 ========== %%
for iG = 1:groups
    Ysel = Y(grouping == groupIDs(iG), :); % 本组行为数据
    Xsel = X(grouping == groupIDs(iG), :); % 本组影像数据
    R0 = Ysel.'*Xsel; % 本组交叉协方差
    if ~exist('R', 'var')
        R = R0; % 第一组直接赋值
    else
        R = [R; R0]; % 其余组按行拼接
    end
end

end